/* SIZE T LIB */
#include "ft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*s1;
	size_t	i;

	i = 0;
	s1 = (char*)b;
	while(i < len)
		s1[i++] = c;
	return (b);
}
