#include "libft.h"

void		ft_putbase(int n, int b)
{
	if (b < 2 || b > 36)
		return ;
	if (b == 2)
		write(1, "0b", 1);
	if (b == 8)
		write(1, "\\", 1);
	if (b == 16)
		write(1, "0x", 1);
	if (n > (b - 1))
		ft_putbase(n / b, b);
	ft_putchar(BASE[n % b]);
}
